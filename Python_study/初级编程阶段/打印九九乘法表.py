i = 1
x = 1
y = 1
while i <= 9: #外层循环
    j = 0 #内层循环打印次数重置
    while j < i: #内层循环
        y = i #每次进入内层循环后，y = i 达到第一列的打印效果
        print(f"{x} * {y} = {x * y}" , end = "  ")
        y += 1 #一行每打印一次，y值+1，达到每行的打印效果
        x += 1 #x每打印一次值+1，效果同上
        j += 1 #内层循环计数器更新
    print(" ") #外层循环打印空格从而换行
    i += 1 #外层计数器更新
    x = 1 #外层循环每循环一次后，x从1开始
