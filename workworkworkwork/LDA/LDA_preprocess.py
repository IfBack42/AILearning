def preprocess_text(text):
    """
    中文文本预处理：分词、去停用词、去除非汉字字符（保留汉字、字母、数字，但过滤纯数字等）
    """
    # 1. 清洗文本：去除URL、特殊符号，但保留汉字、字母、数字、部分标点（避免将词组断开）
    #    这里只做简单清洗，保留汉字、字母、数字、下划线、连字符
    text = re.sub(r'http\S+|www\S+', '', text)
    # 保留中文、字母、数字、常见标点（以便分词）
    text = re.sub(r'[^\u4e00-\u9fa5a-zA-Z0-9\s]', ' ', text)
    # 去除多余空格
    text = re.sub(r'\s+', ' ', text).strip()

    # 2. 分词（使用jieba）
    words = jieba.lcut(text)

    # 3. 过滤停用词、长度<2的词、纯数字
    filtered = []
    for w in words:
        if w in STOP_WORDS:
            continue
        if len(w) < 2:  # 过滤单字（可根据需要保留某些单字，如“人”）
            continue
        if w.isdigit():  # 过滤纯数字
            continue
        # 可以保留部分字母词（如“GDP”），但这里简单处理，保留非纯数字且不在停用词中的词
        filtered.append(w)

    return filtered

# 可选：加载外部停用词表
def load_stopwords(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        for line in f:
            STOP_WORDS.add(line.strip())

